# 이 프로그램에서 A는 시작점, B는 도착점, S는 경유지를 말한다. 나머지는 0으로 표시하는데,
# A와 B, S, 0이 있는 위치는 모두 갈림길이다.
#
# 입력 예시(길의 형태)
#
# A000
# 0000
# 00S0
# 000B
# 출력 예시(최단경로의 경우의 수)
#
# 12
